                                    <p>Com o objetivo de contribuir para a redução das desigualdades sociais, a Maua Social arrecadou uma variedade de itens essenciais, desde alimentos não perecíveis até roupas, produtos de higiene pessoal e materiais escolares. Estudantes, professores e funcionários da universidade se uniram nessa nobre causa, doando generosamente para apoiar a iniciativa.</p>

